NUM_CLASSES = len(LABEL_TO_IDX)

model = models.resnet18(weights=models.ResNet18_Weights.IMAGENET1K_V1)

# Freeze all layers except layer4 + fc (transfer learning)
for name, param in model.named_parameters():
    if 'layer4' not in name and 'fc' not in name:
        param.requires_grad = False

model.fc = nn.Sequential(
    nn.Dropout(0.4),
    nn.Linear(model.fc.in_features, NUM_CLASSES)
)
model = model.to(DEVICE)

trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
total     = sum(p.numel() for p in model.parameters())
print(f"ResNet18 | Trainable params: {trainable:,} / {total:,} ({100*trainable/total:.1f}%)")

NUM_EPOCHS = 20
LR         = 1e-3

criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(filter(lambda p: p.requires_grad, model.parameters()), lr=LR, weight_decay=1e-4)
scheduler = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=NUM_EPOCHS)

train_losses, test_losses = [], []
train_accs,   test_accs   = [], []

def evaluate(loader):
    model.eval()
    all_preds, all_labels, running_loss = [], [], 0.0
    with torch.no_grad():
        for images, labels in loader:
            images, labels = images.to(DEVICE), labels.to(DEVICE)
            outputs = model(images)
            running_loss += criterion(outputs, labels).item()
            all_preds.extend(outputs.argmax(dim=1).cpu().numpy())
            all_labels.extend(labels.cpu().numpy())
    return running_loss / len(loader), accuracy_score(all_labels, all_preds), all_preds, all_labels

print("Starting training...")
for epoch in range(1, NUM_EPOCHS + 1):
    model.train()
    running_loss, correct, total_samples = 0.0, 0, 0

    for images, labels in train_loader:
        images, labels = images.to(DEVICE), labels.to(DEVICE)
        optimizer.zero_grad()
        outputs = model(images)
        loss    = criterion(outputs, labels)
        loss.backward()
        optimizer.step()
        running_loss  += loss.item()
        correct       += (outputs.argmax(dim=1) == labels).sum().item()
        total_samples += labels.size(0)

    scheduler.step()
    train_loss = running_loss / len(train_loader)
    train_acc  = correct / total_samples
    test_loss, test_acc, _, _ = evaluate(test_loader)

    train_losses.append(train_loss); test_losses.append(test_loss)
    train_accs.append(train_acc);    test_accs.append(test_acc)

    if epoch % 2 == 0 or epoch == 1:
        print(f"Epoch [{epoch:2d}/{NUM_EPOCHS}]  "
              f"Train Loss: {train_loss:.4f}  Acc: {train_acc:.4f}  |  "
              f"Test Loss: {test_loss:.4f}  Acc: {test_acc:.4f}")

