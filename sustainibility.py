def extract_label_patch(lat, lon, tif_path, patch_size=128):
    with rasterio.open(tif_path) as src:
        row, col = rowcol(src.transform, lon, lat)
        half = patch_size // 2
        r0, r1 = row - half, row + half
        c0, c1 = col - half, col + half
        if r0 < 0 or c0 < 0 or r1 > src.height or c1 > src.width:
            return None
        window = rasterio.windows.Window(c0, r0, patch_size, patch_size)
        return src.read(1, window=window)

def get_dominant_class(patch):
    flat  = patch.flatten()
    valid = flat[flat > 0]
    if len(valid) == 0:
        return None
    values, counts = np.unique(valid, return_counts=True)
    return int(values[np.argmax(counts)])

